package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {

		int num=7;
		//for-> ctrl+space =>2nd option
		//i++ =post increment
		for (int i = 0; i <num; i++) {
			
			System.out.println("numbers :"+i);
		}
		
		//6,5,4,3,2,1,0
		//i-- =post decrement
		for (int i =num; i>0; i--) {
		System.out.println(i);
			
		}
	}

}
